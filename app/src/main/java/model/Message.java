package model;

public class Message {
    private String userName;
    private String message;


    public String getUserName() {
        return userName;
    }

    public void setUserName(String name) {
        this.userName = name;
    }

    public String getUserMessage() {
        return message;
    }

    public void setUserMessage(String msg) {
        this.message = msg;
    }

}
